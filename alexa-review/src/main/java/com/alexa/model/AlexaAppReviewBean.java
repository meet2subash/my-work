package com.alexa.model;

public class AlexaAppReviewBean {
	private String review;
	private String author;
	private String review_source;
	private String rating;
	private String title;
	private String product_name;
	private String reviewed_date;
	/**
	 * @return the review
	 */
	public String getReview() {
		return review;
	}
	/**
	 * @param review the review to set
	 */
	public void setReview(String review) {
		this.review = review;
	}
	/**
	 * @return the author
	 */
	public String getAuthor() {
		return author;
	}
	/**
	 * @param author the author to set
	 */
	public void setAuthor(String author) {
		this.author = author;
	}
	/**
	 * @return the review_source
	 */
	public String getReview_source() {
		return review_source;
	}
	/**
	 * @param review_source the review_source to set
	 */
	public void setReview_source(String review_source) {
		this.review_source = review_source;
	}
	/**
	 * @return the rating
	 */
	public String getRating() {
		return rating;
	}
	/**
	 * @param rating the rating to set
	 */
	public void setRating(String rating) {
		this.rating = rating;
	}
	/**
	 * @return the title
	 */
	public String getTitle() {
		return title;
	}
	/**
	 * @param title the title to set
	 */
	public void setTitle(String title) {
		this.title = title;
	}
	/**
	 * @return the product_name
	 */
	public String getProduct_name() {
		return product_name;
	}
	/**
	 * @param product_name the product_name to set
	 */
	public void setProduct_name(String product_name) {
		this.product_name = product_name;
	}
	/**
	 * @return the reviewed_date
	 */
	public String getReviewed_date() {
		return reviewed_date;
	}
	/**
	 * @param reviewed_date the reviewed_date to set
	 */
	public void setReviewed_date(String reviewed_date) {
		this.reviewed_date = reviewed_date;
	}
	
}
